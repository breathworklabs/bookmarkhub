/**
 * Check if a word is a tech keyword
 */
export function isTechKeyword(word: string): boolean {
  return TECH_KEYWORDS.has(word.toLowerCase())
}

/**
 * Find all tech keywords in a text
 */
export function findTechKeywords(text: string): string[] {
  const words = text.toLowerCase().split(/\s+/)
  return words.filter((word) => TECH_KEYWORDS.has(word))
}
